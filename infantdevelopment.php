        <h2>Physical Development</h2>
        <ul>
            <li><strong>Milestones:</strong> Track important physical milestones, such as rolling over, sitting up, and crawling.</li>
            <li><strong>Tummy Time:</strong> Engage in tummy time daily to strengthen neck and shoulder muscles.</li>
            <li><strong>Safe Environment:</strong> Create a safe space for your infant to explore as they become more mobile.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Cognitive Development</h2>
        <ul>
            <li><strong>Playtime:</strong> Encourage play with age-appropriate toys that stimulate learning and curiosity.</li>
            <li><strong>Reading Together:</strong> Read to your baby daily to support language development and bonding.</li>
            <li><strong>Interactive Games:</strong> Use simple games like peek-a-boo to promote cognitive skills.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Social and Emotional Development</h2>
        <ul>
            <li><strong>Responsive Parenting:</strong> Respond promptly to your baby’s needs to build trust and emotional security.</li>
            <li><strong>Facial Expressions:</strong> Use varied facial expressions to help your baby recognize emotions.</li>
            <li><strong>Social Interaction:</strong> Arrange playdates or outings to expose your baby to other children and adults.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Language Development</h2>
        <ul>
            <li><strong>Talking and Singing:</strong> Talk and sing to your baby regularly to enhance language skills.</li>
            <li><strong>Encourage Sounds:</strong> Encourage cooing and babbling by responding to your baby’s vocalizations.</li>
            <li><strong>Naming Objects:</strong> Name objects during daily routines to increase vocabulary.</li>
        </ul>
    </section>

