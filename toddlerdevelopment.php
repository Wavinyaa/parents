    <h1>Toddler Development Tips</h1>

    <section class="card">
        <h2>Physical Development</h2>
        <ul>
            <li><strong>Motor Skills:</strong> Encourage activities that promote gross and fine motor skills, like running, jumping, and drawing.</li>
            <li><strong>Active Play:</strong> Provide opportunities for active play to enhance physical fitness and coordination.</li>
            <li><strong>Safety:</strong> Childproof your home to ensure a safe environment as toddlers explore.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Cognitive Development</h2>
        <ul>
            <li><strong>Problem-Solving:</strong> Offer puzzles and age-appropriate games to encourage critical thinking and problem-solving skills.</li>
            <li><strong>Learning Through Play:</strong> Use playtime to introduce concepts like colors, shapes, and numbers.</li>
            <li><strong>Creative Activities:</strong> Encourage creativity with drawing, building blocks, and other hands-on activities.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Social and Emotional Development</h2>
        <ul>
            <li><strong>Sharing and Turn-Taking:</strong> Teach sharing and turn-taking through games and playdates.</li>
            <li><strong>Emotional Expression:</strong> Encourage toddlers to express their feelings using words and recognize emotions in others.</li>
            <li><strong>Routine:</strong> Establish a consistent routine to provide security and predictability.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Language Development</h2>
        <ul>
            <li><strong>Vocabulary Building:</strong> Engage in conversations with your toddler to expand their vocabulary.</li>
            <li><strong>Reading Together:</strong> Read books daily to promote literacy skills and comprehension.</li>
            <li><strong>Songs and Rhymes:</strong> Use songs and nursery rhymes to enhance language and rhythm awareness.</li>
        </ul>
    </section>

    <section class="card">
        <h2>Behavioral Development</h2>
        <ul>
            <li><strong>Positive Reinforcement:</strong> Use positive reinforcement to encourage good behavior and set clear boundaries.</li>
            <li><strong>Redirecting Behavior:</strong> Redirect negative behavior by offering alternative choices and distractions.</li>
            <li><strong>Modeling Behavior:</strong> Be a role model by demonstrating appropriate behaviors and communication skills.</li>
        </ul>
    </section>

    